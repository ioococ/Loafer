package cn.manager.domain;


public class Student {

  private long s_;
  private String sname;
  private long sage;
  private String ssex;


  public long getS_() {
    return s_;
  }

  public void setS_(long s_) {
    this.s_ = s_;
  }


  public String getSname() {
    return sname;
  }

  public void setSname(String sname) {
    this.sname = sname;
  }


  public long getSage() {
    return sage;
  }

  public void setSage(long sage) {
    this.sage = sage;
  }


  public String getSsex() {
    return ssex;
  }

  public void setSsex(String ssex) {
    this.ssex = ssex;
  }

}
