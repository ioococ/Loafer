package cn.manager.domain;


public class Course {

  private long c_;
  private String cname;
  private long t_;


  public long getC_() {
    return c_;
  }

  public void setC_(long c_) {
    this.c_ = c_;
  }


  public String getCname() {
    return cname;
  }

  public void setCname(String cname) {
    this.cname = cname;
  }


  public long getT_() {
    return t_;
  }

  public void setT_(long t_) {
    this.t_ = t_;
  }

}
